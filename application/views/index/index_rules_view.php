					<strong>Reglas</strong>
					<p>Las siguientes reglas son válidas para todos los servidores de Cruxata.com.</p>
					
					<p>Las reglas se han establecido para que los usuarios puedan disfrutar de una experiencia de juego justa en los servidores.</p>
					
					<p>Nótese que las quejas y reclamos acerca de cuentas particulares solo se discutirán con sus propietarios por razones de privacidad.</p>
					
					<p>Para más información, o si tienes preguntas o dudas sobre las reglas, aquí las podrás leer otra vez: <a href="community" title="Reglas y otras explicaciones" style="color:#000">Reglas y otras explicaciones</a></p>
					
					<br />
					<br />
					<strong>1. Cuentas</strong>
					<p>El dueño de una cuenta será siempre el dueño de la dirección de e-mail que fue depositada en la cuenta de juego.</p>
					<p>La cuenta de juego debe ser usada y gestionada exclusivamente por su titular. La única excepción permitida es el "cuidado de cuenta" (account sitting).</p>
					<p>El cambio de una cuenta dentro de un servidor debe ser efectuada con la asistencia y bajo la supervisión del servicio de soporte.</p>
					<p>Cuando una cuenta ha sido cedida, deben pasar por lo menos 15 días antes que ésta pueda ser cedida de nuevo. De acuerdo con nuestros Términos y Condiciones, el nuevo titular de la cuenta debe cambiar la dirección de e-mail durante las primeras 12 horas tras el cambio. Puedes ponerte en contacto con el servicio de soporte a través de http://support.cruxata.com.</p>
					
					<br />
					<br />
					<strong>2. Multi-cuentas</strong>
					<p>A cada jugador se le permite jugar con una sola cuenta por servidor.</p>
					<p>En caso de que dos o más cuentas estén siendo jugadas, ya sea ocasionalmente, habitualmente o permanentemente, desde la misma conexión de Internet (por ejemplo: desde el colegio, universidad o Internet café), se aconseja comunicarlo preventivamente a uno de los Operadores de Juego. Puedes ponerte en contacto con el servicio de soporte en http://support.cruxata.com.</p>
					
					<br />
					<br />
					<strong>3. Cuidado de cuentas (Account sitting)</strong>
					<p>El "sitting" permite confiarle a otro jugador el cuidado de la propia cuenta. En este caso, antes de que el "sitting" tenga lugar, será necesario informarle previamente a un Operador de Juego a través del servicio de soporte http://support.cruxata.com.</p>
					<p>Una cuenta puede ser cuidada durante un período máximo de 24 horas.</p>
					<p>El período de "sitting" se considera finalizado apenas el titular de la cuenta inicie sesión.</p>
					<p>Durante este período no es posible lanzar ataques a otros jugadores. Sólo está permitido cuidar de una cuenta que no haya sido cuidada en los últimos 7 días y si el propietario ha iniciado sesión con su cuenta alguna vez desde el "sitting" anterior.</p>
														
					<br />
					<br />	
					<strong>4. Uso de bugs / Uso de scripts</strong>
					<p>Está terminantemente prohibido aprovecharse de un error de programación o de bugs, así como omitir la existencia de un bug.</p>
					<p>Además, está estrictamente prohibido la utilización de programas de interfaz entre los jugadores y el juego, o de programas que generen automáticamente informaciones para dar ventajas injustas a un grupo de jugadores.</p>
					
					<br />
					<br />
					<strong>5. Amenazas en la vida real</strong>
					<p>Está prohibido insinuar que se va a localizar y causar daño a un jugador, a un miembro del equipo o a cualquier persona.</p>
					
					<br />
					<br />
					<strong>6. Insultos y spam</strong>
					<p>Cualquier tipo de insulto y de spam está prohibido.</p>
					
					<br />
					<br />
					<strong>7. Idioma</strong>
					<p>Cada servidor corresponde a un idioma. El moderador del servidor se reserva el derecho de excluir del juego a los jugadores que no son capaces de hablar la respectiva lengua del juego.</p>
					
					<br />
					<br />
					<strong>8. Infracciones a las reglas antes citadas</strong>
					<p>Conforme a su gravedad, las infracciones a las reglas antes citadas serán sancionadas con amonestaciones o con la completa expulsión del juego.</p>
					<p>Los moderadores de juego responsables determinan el tipo y la duración de la sanción y son las personas de contacto en caso de bloqueos de cuenta.</p>
					
					<br />
					<br />
					<strong>9. Términos y Condiciones</strong>
					<p>Los Términos y Condiciones están complementados por estas reglas, por tanto también deben ser acatados.</p>
					
					<br />
					<br />
					<strong>10. Excepciones</strong>
					<p>El Equipo de Administración de la Comunidad se reserva el derecho de hacer excepciones sobre estas reglas. En casos especiales (por ejemplo durante los eventos) las reglas del juego pueden ser cambiadas o suspendidas. Los jugadores afectados serán siempre informados sobre ello.</p>
